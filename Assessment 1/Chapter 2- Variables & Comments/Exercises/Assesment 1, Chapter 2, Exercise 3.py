# Define a name variable 
name = "\t\n Shahab \t\n"

# Print the name with surrounding whitespace
print("Name with whitespace:")
print(name)

# Use lstrip() to remove whitespace on the left
print("\nUsing lstrip():")
print(name.lstrip())

# Use rstrip() to remove whitespace on the right
print("\nUsing rstrip():")
print(name.rstrip())

# Use strip() to remove whitespace on both the right and left
print("\nUsing strip():")
print(name.strip())
