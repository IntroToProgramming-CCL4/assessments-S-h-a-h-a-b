# Create a dictionary to store information about a person
person_info = {"First_name": "Shahab","Last_name": "Mughal","Age": 18,"City": "Dubai"}

# Accessing Values
First_name = person_info['First_name']
Last_name = person_info['Last_name']
Age = person_info['Age']
City = person_info['City']

# Now print each piece of information
print("First Name:", First_name)
print("Last Name:", Last_name)
print("Age:", Age)
print("City:", City)